<?php 

//Use ++ increment
$counter = 10;
$counter +=1;
echo $counter . PHP_EOL;

//Post increment operator
echo $counter++ . PHP_EOL;
echo $counter . PHP_EOL;


//Pre increment operator
echo ++$counter . PHP_EOL;
echo $counter . PHP_EOL;

//Use -- decrement
$counter = 10;
$counter -=1;
echo $counter . PHP_EOL;


//Post decrement operator
echo $counter-- . PHP_EOL;
echo $counter . PHP_EOL;


//Pre decrement operator
echo --$counter . PHP_EOL;
echo $counter . PHP_EOL;